    public static class SqlCeUtilities
    {
        private readonly static IDictionary<SqlCeUtilities.SqlCeNativeErrorCodes, Action<string>>
            SqlCeNativeErrorHandlers;

        static SqlCeUtilities()
        {
            Dictionary<SqlCeUtilities.SqlCeNativeErrorCodes, Action<string>> sqlCeNativeErrorCodes =
                new Dictionary<SqlCeUtilities.SqlCeNativeErrorCodes, Action<string>>()
                {
                    {
                        SqlCeUtilities.SqlCeNativeErrorCodes.SSCE_M_DBUPGRADENEEDED,
                        new Action<string>(SqlCeUtilities.HandleDbUpgradeNeeded)
                    }
                };
            SqlCeUtilities.SqlCeNativeErrorHandlers = sqlCeNativeErrorCodes;
        }

        public static SqlCeConnection GetConnection(string connectionString, bool open = true)
        {
            SqlCeConnection sqlCeConnection = null;
            for (int i = 0; i < 2; i++)
            {
                try
                {
                    sqlCeConnection = new SqlCeConnection(connectionString);
                    sqlCeConnection.Open();
                    break;
                }
                catch (SqlCeInvalidDatabaseFormatException sqlCeInvalidDatabaseFormatException)
                {
                    SqlCeUtilities.SqlCeNativeErrorCodes obj =
                        (SqlCeUtilities.SqlCeNativeErrorCodes)Enum.ToObject(
                            typeof(SqlCeUtilities.SqlCeNativeErrorCodes),
                            sqlCeInvalidDatabaseFormatException.NativeError);
                    if (!SqlCeUtilities.SqlCeNativeErrorHandlers.ContainsKey(obj))
                    {
                        throw;
                    }

                    SqlCeUtilities.SqlCeNativeErrorHandlers[obj](connectionString);
                }
            }

            if (sqlCeConnection == null)
            {
                throw new NullReferenceException("Connection cannot be null.");
            }

            if (!open)
            {
                sqlCeConnection.Close();
            }

            return sqlCeConnection;
        }

        private static void HandleDbUpgradeNeeded(string connectionString)
        {
            using (SqlCeEngine sqlCeEngine = new SqlCeEngine(connectionString))
            {
                sqlCeEngine.Upgrade();
            }
        }

